        <h1>About Us</h1>

        <p>We aim at bringing the important news and information to the public, providing an innovative, secure and anonymous way for sources to leak information. The most important activities is to publish original source material alongside the other news stories so readers can see evidence of the truth.</p>

        <p>We accept leaked material from public. We do not ask for material, but we make sure that if material is going to be submitted it is done securely and that the source is well protected. Because we receive sensitive information, and we have limited resources, it may take time to review a source’s submission.</p>

        <p>Our news stories are in the comfortable presentation style of Wikipedia, although the two organizations are not otherwise related. Unlike Wikipedia, random readers cannot edit our source documents.</p>

        <p>OpenLeaks will provide a new model of journalism. Because we are not motivated by making a profit, we work cooperatively with the public. </p>

        <p>We assess all news stories and test their authenticity. We send a submitted document through a very detailed examination procedure. Publishing the original source material behind each of the stories is the way in which we show the public that the story is authentic. Readers don’t have to take our word for it; they can see for themselves.</p>

        <p>OpenLeaks is created by a group of individuals whose aim is to bring the unlawful acts from the darkness to the light without leaking the personal information of the person providing the leaks. Global group of people that includes software programmers, network engineers, mathematicians and others came together with an idea to provide a platform where everyone can anonymously upload the leaks without any fear. It is clear by now that OpenLeaks is not a front end of any intelligence agency instead it is a part of  Smart City program to provide a digital platform for data leaks.  </p>


